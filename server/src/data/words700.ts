function dedupeRaw(
  items: typeof RAW,
): typeof RAW {
  const seen = new Set<string>()
  return items.filter(([word]) => {
    if (seen.has(word)) return false
    seen.add(word)
    return true
  })
}

const rawWords = new Set(RAW.map(([w]) => w))
const MERGED_RAW = [...RAW, ...EXTRA_VOCAB_RAW.filter(([w]) => !rawWords.has(w))]

export const VOCAB_ITEMS: VocabItem[] = dedupeRaw(MERGED_RAW).map(([word, type, category, meaning, usage, example, confusableNote], i) => ({
  id: `vocab-${i + 1}`,
  word,
  type,
  category,
  meaning,
  usage,
  example,
  confusable: confusableNote ? [confusableNote] : undefined,
  frequency: i < 120 ? 'core' : 'high',
}))

export const VOCAB_CATEGORY_MAP = Object.fromEntries(
  VOCAB_CATEGORIES.map((c) => [c.id, c]),
) as Record<string, VocabCategory>

export function getVocabByCategory(categoryId?: string): VocabItem[] {
  if (!categoryId) return VOCAB_ITEMS
  return VOCAB_ITEMS.filter((v) => v.category === categoryId)
}

export function searchVocab(keyword: string): VocabItem[] {
  const q = keyword.trim().toLowerCase()
  if (!q) return VOCAB_ITEMS
  return VOCAB_ITEMS.filter(
    (v) =>
      v.word.includes(q) ||
      v.meaning.includes(q) ||
      v.example?.includes(q),
  )
}
